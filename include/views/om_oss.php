		<h3>Vilka är vi?</h3>
		Vi som skapade och genomförde projektet bokloket.se är Tobias Näslund Eriksson, Martin Skiöld och Isak Eriksson,
		tre studenter på Kandidatprogrammet i Systemvetenskap vid Uppsala universitet.
		<h3>Bokloket.se - projektet och konceptet</h3>
		Hemsidan och konceptet bokloket.se startade under kursen E-tjänster och webbprogrammering som vi tre läste
		vårterminen 2014. Projektet började som en idé om en bokklubb online där användare via sidan kunde nå andra
		användare och sedan utanför hemsidan byta böcker via annonsering. Inte helt överraskande kom idén från
		problemet med dyr kurslitteratur som Uppsalas studenter inte hade användning för mer än ett par månader.
		En sådan sida skulle kunna leda till billigare, till och med gratis kurslitteratur.<br>
		Vi förstod	snabbt att en e-tjänst begränsad till byte av böcker inte skulle nå särskilt långt och började tidigt i projektet
		diskutera möjligheterna kring att utöka e-tjänsten så att den nu även täckte annonser för försäljning av böcker.
		<h3>Feedback</h3>
