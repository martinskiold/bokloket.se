		<h2>Bokloket.se:s regler</h2>
		<hr>
		Genom att använda tjänsterna på bokhyllan.se godkänner användaren de Allmänna Villkor
		som är uppsatta av bokhyllan.se och förbinder sig att följa dem.
		<h2>Allmänna villkor</h2>
		Registrering av en ny annons innebär i grunden ett godkännande av medlemmen att annonsen
		får granskas av tjänsteleverantören. Denna granskning måste godkännas innan annonsen
		slutligen publiceras. Detta görs för att kontrollera att annonsen följer de regler som är uppsatta
		av tjänsteleverantören. Annonser som inte följer reglerna kommer ej att publiceras och
		användaren uppmanas att ändra annonsen. 
		<h3>Språk</h3>
		De två tillåtna språken på bokhyllan.se är Svenska och Engelska med tillhörande språkvariationer.
		Språk får inte väcka anstöt eller verka stötande mot vissa folkgrupper/invdivider.
		Annonsera med vårdat språk.
		<h3>Annonsinnehåll</h3>
		Håll annonstext och rubrik relaterad till varan som ska annonseras. Annonsrubriken får inte vara
		hänvisande eller missvisande. Med hänvisande menas text som uppmanar läsare att agera på
		något sätt, t.ex. att navigera till ny hemsida. En annons får maximalt innehålla en vara. Annonsen
		måste innehålla bokens titel, författare, genre och språk.
		<h3>Bilder</h3>
		Uppladdade bilder måste vara relaterad till annonsen och får absolut inte vecka anstöt. Bilder
		som enligt svensk lag är olagliga får ej laddas upp i annonsen. Utöver detta förbjuds
		pornografiska bilder på bokhyllan.se.
		<h3>Kontakt med andra part</h3>
		Kontakt med den andra parten sker helt på egen risk och utanför bokhyllan.se juridiska område.
		Medlemmar som har rapporterats missbruka sin rätt att kontakta personer via vår hemsida
		utesluts som medlemmar på bokhyllan.se. Samma gäller även läsare. Dessa blockeras från
		hemsidan genom andra åtgärder.
		<h3>Tvister</h3>
		Tvister som uppkommit på grund av användandet av vår tjänst avgörs i svensk allmän domstol.
		<h3>Spamming och robotgenererat innehåll</h3>
		Spamming tillåts ej på e-tjänsten. Robotgenrerat innehåll och robotgenererade medlemskonton
		är likaså otillåtet.
		<h3>Stötande innehåll</h3>
		Annonsen får inte på något sätt verka stötande eller kränkande mot folkgrupper och/eller
		enskilda individer. Ålderdomliga uttryck och/eller begrepp kan uppfattas som kränkande av
		vissa folkgrupper och bör därför avlägsnas från annonsen.
		<h3>Olagliga annonsvaror</h3>
		Varor som enligt svensk lag är olagliga får inte annonseras på bokhyllan.se. Detta innefattar
		bland annat stöldgods.
		<h3>Tillåtna annonsvaror</h3>
		De enda varor vi på bokhyllan.se tillåter är böcker och pocketböcker. Det är inte tillåtet att
		annonsera böcker som inte existerar eller som inte tillhandahålls av dig som medlem.
		<h3>Länkar</h3>
		Länkhänvisningar är förbjudna då dessa kan hänvisa användare till stötande hemsidor
		och/eller hemsidor med skadlig kod. Det är utöver länkhänvisningar också otillåtet att med
		vanligt språk referera till externa sidor.
